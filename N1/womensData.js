function display(womensData){
  document.querySelector("#container").innerHTML="";
  womensData.map(function(elem,index,array){
    var box = document.createElement("div");
    box.setAttribute("class","box");
    var sbox = document.createElement("div");
    sbox.setAttribute("class","price");
    var img = document.createElement("img");
    img.setAttribute("src", elem.image_url)
    var name = document.createElement("p");
    name.innerText = elem.name;
    var price = document.createElement("h4");
    price.innerText = elem.price;
    var stprice = document.createElement("s");
    stprice.innerText = elem.strikedoffprice;
    var button = document.createElement("button");
    button.setAttribute("id","Addbutton");
    button.innerText = "Add to cart";
    sbox.append(price,stprice);
    box.append(img,name,sbox,button);
    document.querySelector("#container").append(box);
    button.addEventListener("click", addToCart)
    function addToCart(){
    addCartObj = {
      img:elem.image_url,
      name:elem.name,
      price:elem.price,
      stprice:elem.strikedoffprice
    }
    console.log(addCartObj)
    cartArr.push(addCartObj);
    
    console.log(cartArr);
    localStorage.setItem("cartItems",JSON.stringify(cartArr))
    
    }
    
    
    });
}



function PriceSort(){
  var selected = document.querySelector("#PriceSort").value 
 if(selected==="L2H"){
  womensData.sort(function(a,b){
         return a.price-b.price;
     }); 
     display(womensData)

 }
 if(selected==="H2L"){
  womensData.sort(function(a,b){
         return b.price-a.price;
     });
     display(womensData)
 }
 }
 function AlphaSort(){
  var selected = document.querySelector("#select").value 
 if(selected==="A2D"){
  womensData.sort(function(a,b){
         if(a.name>b.name)
         return 1;
         if(a.name<b.name)
         return -1;
     }); 
     display(womensData)
 }
 if(selected==="D2A"){
  womensData.sort(function(a,b){
    if(b.name>a.name)
    return -1;
    if(b.name<a.name)
    return -1;
    });
     display(womensData)
 }
 }

